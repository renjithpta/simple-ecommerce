import asyncHandler from 'express-async-handler'
import Order from '../models/orderModel.js'
import TxDetails from '../models/txDetailsModel.js'
import { rewardToken } from '../utils/tokenUtil.js'

// @desc    Create new order
// @route   POST /api/orders
// @access  Private
const addOrderItems = asyncHandler(async (req, res) => {
  const {
    orderItems,
    shippingAddress,
    paymentMethod,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
  } = req.body

  if (orderItems && orderItems.length === 0) {
    res.status(400)
    throw new Error('No order items')
    return
  } else {
    const order = new Order({
      orderItems,
      user: req.user._id,
      shippingAddress,
      paymentMethod,
      itemsPrice,
      taxPrice,
      shippingPrice,
      totalPrice,
    })

    const createdOrder = await order.save()

    res.status(201).json(createdOrder)
  }
})

// @desc    Get order by ID
// @route   GET /api/orders/:id
// @access  Private
const getOrderById = asyncHandler(async (req, res) => {
  const order = await Order.findById(req.params.id).populate(
    'user',
    'name email'
  )

  if (order) {
    res.json(order)
  } else {
    res.status(404)
    throw new Error('Order not found')
  }
})

// @desc    Update order to paid
// @route   GET /api/orders/:id/pay
// @access  Private
const updateOrderToPaid = asyncHandler(async (req, res) => {
  const order = await Order.findById(req.params.id)
  let price = 0;
  for (let i = 0; i < order.orderItems.length; i++) {
    price = price + (order.orderItems[i].price * order.orderItems[i].qty)
  }

  console.log(order, "*******call reward token *******", req.user.email, 1, parseInt(price))
  let result = await rewardToken(req.user.email, 1, parseInt(Math.round(price)))
  console.log("After", result.hash.transactionHash)
  let obj = {
    email: req.user.email,
    transactionHash: result.hash.transactionHash,
    blockHash: result.hash.blockHash,
    totalItemsPrice: price,
    order: order._id,
  };
  console.log("****************", obj);

  const txDetails = new TxDetails({
    email: req.user.email,
    transactionHash: result.hash.transactionHash,
    blockHash: result.hash.blockHash,
    totalItemsPrice: parseInt(price),
    order: order._id,
  })
  console.log("After Object", txDetails)
  await txDetails.save()
  console.log("save after Object", txDetails)
  if (order) {
    order.isPaid = true
    order.paidAt = Date.now()
    order.paymentResult = {
      id: req.body.id,
      status: req.body.status,
      update_time: req.body.update_time,
      email_address: req.body.payer.email_address,
    }
    const updatedOrder = await order.save()
    res.json(updatedOrder)
  } else {
    res.status(404)
    throw new Error('Order not found')
  }
})

// @desc    Update order to delivered
// @route   GET /api/orders/:id/deliver
// @access  Private/Admin
const updateOrderToDelivered = asyncHandler(async (req, res) => {
  const order = await Order.findById(req.params.id)

  if (order) {
    order.isDelivered = true
    order.deliveredAt = Date.now()

    const updatedOrder = await order.save()

    res.json(updatedOrder)
  } else {
    res.status(404)
    throw new Error('Order not found')
  }
})

